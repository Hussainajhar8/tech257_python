x = 5
y = 1

print("arithmetic operators")
result = x + y + 2
print(f"Let's show {x} + {y} + 2 = {result}")
print(type(result))
result = x - y - 2
print(f"Let's show {x} - {y} - 2 = {result}")
print(type(result))
result = x * y * 2
print(f"Let's show {x} * {y} * 2 = {result}")
print(type(result))
result = x / y / 2
print(f"Let's show {x} / {y} / 2 = {result}")
print(type(result))
result = x % y # gives the remainder
print(f"Let's show {x} % {y} = {result}")
print(type(result), "\n")

print("comparison operators")
result = x > y
print(f"Let's show {x} > {y} = {result}")
print(type(result))
result = x >= y
print(f"Let's show {x} >= {y} = {result}")
print(type(result))
result = x < y
print(f"Let's show {x} < {y} = {result}")
print(type(result))
result = x <= y
print(f"Let's show {x} <= {y} = {result}")
print(type(result))
result = x == y
print(f"Let's show {x} == {y} = {result}")
print(type(result))
result = x != y
print(f"Let's show {x} != {y} = {result}")
print(type(result))